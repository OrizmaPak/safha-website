                <div class="faq-list">
                    <details open>
                        <summary>Which hotel operations does SAFHA cover?<i class="fas fa-plus" aria-hidden="true"></i></summary>
                        <p>SAFHA covers administration, inventory, front office, cashier and folios, point of sale, food and beverage, purchasing, housekeeping, maintenance, accounts, personnel and payroll, plus dashboard reporting.</p>
                    </details>
                    <details>
                        <summary>Can different departments use the same system?<i class="fas fa-plus" aria-hidden="true"></i></summary>
                        <p>Yes. Departments work in connected functional areas, while access control lets administrators choose which screens and actions each user can reach.</p>
                    </details>
                    <details>
                        <summary>Does SAFHA handle group and corporate business?<i class="fas fa-plus" aria-hidden="true"></i></summary>
                        <p>Yes. The platform includes group reservations and check-in, company and travel agency records, plus dedicated guest, company, agency and group folios.</p>
                    </details>
                    <details>
                        <summary>What accounting tools are included?<i class="fas fa-plus" aria-hidden="true"></i></summary>
                        <p>SAFHA includes GL account setup, balanced journal entry, transaction history, trial balance, income statement, balance sheet and a general sales report, alongside receivable and payable tracking.</p>
                    </details>
                    <details>
                        <summary>Can SAFHA support housekeeping and maintenance?<i class="fas fa-plus" aria-hidden="true"></i></summary>
                        <p>Yes. Teams can manage room status, daily housekeeping assignments, cleaning checklists and lost-and-found records, while maintenance requests can be assigned and developed into work orders.</p>
                    </details>
                    <details>
                        <summary>How do we know whether SAFHA fits our property?<i class="fas fa-plus" aria-hidden="true"></i></summary>
                        <p>Request a demonstration and share your property type, room count and priority workflows. The SAFHA team can then focus the session on the parts of the platform that matter to your operation.</p>
                    </details>
