                    <p class="eyebrow"><span aria-hidden="true"></span>Questions, answered</p>
                    <h2>What hotels ask before seeing SAFHA.</h2>
                    <p>The best way to evaluate SAFHA is against your own operating flow. These answers cover the platform scope before a tailored demonstration.</p>
                    <a class="button button-secondary" href="#demo">Discuss your property</a>
